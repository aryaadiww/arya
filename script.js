document.addEventListener("DOMContentLoaded", () => {
    const cube = document.querySelector(".cube");
    let rotateX = 0;
    let rotateY = 0;

    setInterval(() => {
        rotateX += 1;
        rotateY += 1;
        cube.style.transform = `rotateX(${rotateX}deg) rotateY(${rotateY}deg)`;
    }, 30);
});

